import ACTION from '../actionTypes';

//----------------------------------------------------------------------------------------------------
// refreshTodos
//----------------------------------------------------------------------------------------------------
export const refreshTodos = (fromDate, toDate, todos) => {
  return {
    type: ACTION._REFRESH,
    fromDate,
    toDate,
    todos,
  };
};
//----------------------------------------------------------------------------------------------------
// putOnTodo
//----------------------------------------------------------------------------------------------------
export const putOnTodo = (todo) => {
  return {
    type: ACTION._PUT_ON,
    todo,
  };
};
//----------------------------------------------------------------------------------------------------
// rerenderTodos
//----------------------------------------------------------------------------------------------------
export const rerenderTodos = (todos) => {
  return {
    type: ACTION._RERENDER,
    todos,
  };
};
//----------------------------------------------------------------------------------------------------
// takeOffTodos
//----------------------------------------------------------------------------------------------------
export const takeOffTodos = (todos) => {
  return {
    type: ACTION._TAKE_OFF,
    todos,
  };
};
//----------------------------------------------------------------------------------------------------
// selectTodos
//----------------------------------------------------------------------------------------------------
export const selectTodos = (todos, selected) => {
  return {
    type: ACTION._SELECT,
    todos,
    selected,
  };
}
//----------------------------------------------------------------------------------------------------
// sortTodos
//----------------------------------------------------------------------------------------------------
export const sortTodos = (sortingKey) => {
  return {
    type: ACTION._SORT,
    sortingKey,
  };
};
